//Number operation for a number
let num = "20"
console.log(typeof num)

let numValue = Number(num)
console.log(typeof numValue)
console.log(numValue)

//Number operation for a string
let numWithLetter = "20abc"
console.log(typeof numWithLetter)

let numLetterValue = Number(numWithLetter)
console.log(typeof numLetterValue)
console.log(numLetterValue)

